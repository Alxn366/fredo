// Language switching functionality
document.addEventListener("DOMContentLoaded", function () {
  // Set initial language
  setLanguage("ku");

  // Language selector event
  document
    .getElementById("language-selector")
    .addEventListener("change", function () {
      const lang = this.value;
      setLanguage(lang);
    });
});

function setLanguage(lang) {
  // Update all elements with data-translate attribute
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Update placeholder texts
  document
    .querySelectorAll("[data-translate-placeholder]")
    .forEach((element) => {
      const key = element.getAttribute("data-translate-placeholder");
      if (translations[lang][key]) {
        element.placeholder = translations[lang][key];
      }
    });

  // Set HTML direction for Arabic
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.documentElement.lang = lang;
}

// Countdown Timer (365 days)
function updateCountdown() {
  const now = new Date();
  
  const endDate = new Date("2026-08-06T23:59:59");
   

  const distance = endDate - now;

  const days = Math.floor(distance / (1000 * 60 * 60 * 24));
  const hours = Math.floor(
    (distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)
  );
  const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((distance % (1000 * 60)) / 1000);

  if (document.getElementById("days")) {
    document.getElementById("days").textContent = days
      .toString()
      .padStart(2, "0");
    document.getElementById("hours").textContent = hours
      .toString()
      .padStart(2, "0");
    document.getElementById("minutes").textContent = minutes
      .toString()
      .padStart(2, "0");
    document.getElementById("seconds").textContent = seconds
      .toString()
      .padStart(2, "0");
  }

  if (distance < 0) {
    clearInterval(countdownTimer);
    const countdownContainer = document.querySelector(".countdown-container");
    if (countdownContainer) {
      const currentLang = document.getElementById("language-selector").value;
      countdownContainer.innerHTML = `<h2>${translations[currentLang]["offer_ended"]}</h2>`;
    }
  }
}

let countdownTimer = setInterval(updateCountdown, 1000);
updateCountdown(); // Initial call

// Fredo Demo
document.getElementById("fredo-btn").addEventListener("click", function () {
  const currentLang = document.getElementById("language-selector").value;
  const messages = [
    translations[currentLang]["feature1"],
    translations[currentLang]["feature2"],
    translations[currentLang]["feature3"],
    translations[currentLang]["feature4"],
  ];
  const messageDiv = document.getElementById("fredo-message");
  messageDiv.innerHTML = "";

  let i = 0;
  const messageInterval = setInterval(() => {
    if (i < messages.length) {
      const messageElement = document.createElement("p");
      messageElement.textContent = `Fredo: ${messages[i]}`;
      messageElement.style.opacity = "0";
      messageElement.style.animation = "fadeIn 0.5s forwards";

      messageDiv.appendChild(messageElement);
      messageDiv.scrollTop = messageDiv.scrollHeight;
      i++;
    } else {
      clearInterval(messageInterval);
    }
  }, 2000);
});

